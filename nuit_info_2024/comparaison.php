    <h3>1. Augmentation de la température</h3>
    <p><strong>Océans :</strong> Le réchauffement climatique entraîne une augmentation de la température des eaux, provoquant la décoloration des coraux (blanchiment) et perturbant les écosystèmes marins, ce qui menace la biodiversité.</p>
    <p><strong>Corps humain :</strong> Une fièvre (élévation de la température corporelle) survient lorsque le corps combat une infection. Si elle devient trop élevée, elle peut causer des dommages graves, comme la dégradation des protéines et la défaillance des organes.</p>
    
    <h3>2. Acidification</h3>
    <p><strong>Océans :</strong> L'absorption accrue de dioxyde de carbone (CO₂) rend les eaux plus acides, ce qui affecte la capacité des organismes marins, comme les coquillages et les coraux, à former leurs structures calcaires.</p>
    <p><strong>Corps humain :</strong> Une acidification du sang (acidose) peut se produire en cas de troubles respiratoires ou métaboliques. Cela perturbe les fonctions enzymatiques et peut être fatal si non traité.</p>

    <h3>3. Perturbation des courants</h3>
    <p><strong>Océans :</strong> Le réchauffement planétaire ralentit des courants essentiels comme la circulation thermohaline, perturbant les cycles climatiques globaux et affectant les écosystèmes.</p>
    <p><strong>Corps humain :</strong> Une mauvaise circulation sanguine due à des problèmes cardiovasculaires entraîne des dysfonctionnements dans la distribution de l’oxygène et des nutriments, provoquant des troubles comme la fatigue chronique ou des accidents vasculaires.</p>

    <h3>4. Montée des eaux et rétention hydrique</h3>
    <p><strong>Océans :</strong> La fonte des glaces et la dilatation thermique de l’eau entraînent une élévation du niveau des mers, menaçant les habitats côtiers.</p>
    <p><strong>Corps humain :</strong> La rétention d’eau (œdème) peut survenir en cas de déséquilibre dans le système rénal ou lymphatique, causant un gonflement et une pression excessive sur les organes.</p>

    <h3>5. Perte d’oxygène</h3>
    <p><strong>Océans :</strong> Le réchauffement entraîne des zones de moindre oxygénation (hypoxie), rendant certaines régions marines inhabitées pour la vie aquatique.</p>
    <p><strong>Corps humain :</strong> L’hypoxie (manque d’oxygène dans le sang) provoque une fatigue extrême, des lésions cellulaires, voire une défaillance des organes si elle persiste.</p>

    <h3>6. Pollution et accumulation de toxines</h3>
    <p><strong>Océans :</strong> L'accumulation de plastique, de métaux lourds et de produits chimiques perturbe les chaînes alimentaires et intoxique les espèces marines.</p>
    <p><strong>Corps humain :</strong> L'exposition chronique aux toxines (pollution, aliments transformés, tabac) surcharge le foie et les reins, conduisant à des maladies chroniques comme le cancer ou l'insuffisance rénale.</p>

    <h3>7. Changements brusques et catastrophes</h3>
    <p><strong>Océans :</strong> Les événements climatiques extrêmes, comme les tempêtes et les ouragans, sont exacerbés par les changements climatiques, mettant en péril les communautés côtières.</p>
    <p><strong>Corps humain :</strong> Les crises aigües, comme les infarctus ou les AVC, peuvent être déclenchées par un stress soudain ou une surcharge des systèmes vitaux.</p>

    <h3>8. Diminution de la résilience</h3>
    <p><strong>Océans :</strong> Les écosystèmes marins deviennent moins capables de se régénérer face aux perturbations, comme la surpêche ou le réchauffement.</p>
    <p><strong>Corps humain :</strong> Avec l'âge ou en cas de stress chronique, le système immunitaire et les capacités de récupération du corps s’affaiblissent.</p>

    <h3>Synthèse</h3>
    <p>Les changements climatiques sur les océans et les maladies chroniques sur le corps humain partagent une dynamique similaire : une surcharge de stress et une perte d’équilibre. Préserver les océans, tout comme prendre soin de son corps, nécessite d'agir en amont en adoptant des comportements responsables (réduction des émissions pour les océans, hygiène de vie pour le corps). Ces deux systèmes, vitaux pour la planète et l’humanité, sont interdépendants et vulnérables à nos actions.</p>
